     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'video' => ['required']
        ]);

        $path = $request->file('video')->store('video');

        $video = new Video();
        $video->convert($path);

        return back();
    }

